public interface SweetBox {

    // Метод для добавления сладости в коробку
    // sweet - объект сладости, который нужно добавить
    void addSweet(Sweet sweet);

    // Метод для удаления последней добавленной сладости из коробки
    // (удаляет элемент с конца списка)
    void removeLastSweet();

    // Метод для удаления сладости по указанному индексу
    // index - позиция элемента, который нужно удалить (начинается с 0)
    void removeSweetByIndex(int index);

    // Метод для получения общего веса всех сладостей в коробке
    // Возвращает суммарный вес в виде числа с плавающей точкой
    double getTotalWeight();

    // Метод для получения общей стоимости всех сладостей в коробке
    // Возвращает суммарную цену в виде числа с плавающей точкой
    double getTotalPrice();

    // Метод для печати информации о всех сладостях в коробке
    // (обычно выводит список в консоль)
    void printAllSweets();

    // Метод для оптимизации содержимого коробки по весу
    // maxWeight - максимально допустимый суммарный вес коробки
    // Удаляет сладости, пока общий вес не станет <= maxWeight
    void optimizeByWeight(double maxWeight);

    // Метод для оптимизации содержимого коробки по цене
    // maxWeight - максимально допустимый суммарный вес коробки
    // Удаляет самые дешевые сладости, пока вес не станет <= maxWeight
    void optimizeByPrice(double maxWeight);
}

